defmodule Day15Test do
  use ExUnit.Case
  doctest Day15

  test "part 1 with example" do
    assert Day15.part1(small_example()) == 2028
    assert Day15.part1(example()) == 10092
  end

  test "part 1 with my input data" do
    assert Day15.part1(input()) == 1448589
  end

  test "part 2 with example" do
    assert Day15.part2(small_example2()) == 618
    assert Day15.part2(example()) == 9021
